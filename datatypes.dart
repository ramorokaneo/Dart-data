// Dart program demonstrating data types

void main() {
  // int data type
  int age = 30;
  print('Age: $age');

  // double data type
  double height = 5.11;
  print('Height: $height');

  // String data type
  String name = 'Neo Ramoroka';
  print('Name: $name');

  // List data type
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, int> marks = {'Math': 90, 'Science': 85, 'English': 88};
  print('Marks: $marks');
}
